
## numeric -> echivalent cu float din python
a <- 2
print(a)
print(class(a))

# numeric  -> echivalent cu float din python
a <- 2.3
print(a)
print(class(a))

# integer -> echivalent cu integer (se adauga L)
b <- 19L
print(b)
print(class(b))

## logical  boolean din python
c <- TRUE
print(c)
print(class(c))

# character -> echivalent cu string
d <- "telefon"
print(d)
print(class(d))

d <- 'telefon'
print(d)
print(class(d))

d <- 'tele" \n fon'
print(d)
print(class(d))

# complexe
e = 10 + 3i
print(e)
print(class(e))


f = 1i
patrat_f <- f ^ 2
print(patrat_f)

